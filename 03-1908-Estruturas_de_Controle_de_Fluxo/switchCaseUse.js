
let mes = 5;
switch (mes) {
    case 1:
        console.log('Janeiro');
        break;
    case 2:
        console.log('Fevereiro');
        break;
    case 3:
        console.log('Março');
        break;
    default:
        console.log('Mes não é Janeiro, Fevereio ou Março');
}
console.log("Resultado do SWITCH CASE")